<h2>What kind of stuff will you find here?</h2>
<p>The Adonis program wasn't developed out of popular workouts of the times, or because that's what people have been doing for thousands of years. The Adonis Program is highly scientific, tested, and results oriented. As such, at the end of mountains of research and study they end up with some exercises that work for most everybody, but there is a lot more to it than that. And depending on what what you are looking for, you may benefit from reading into the content their team has put together.</p>
<p> I would highly suggest starting with <a href="/_THE_THEORY_OF_IDEAL_BODY_PROPORTIONS_AGR.pdf">The Theory of the Ideal Body Proportions</a>. It reads kind of like the authors personal story from first starting to workout, following the worlds ideals of fitness, being disillusioned with impossible proportions (without drugs), and finally how and why he developed this program. Along the way you learn some interesting facts about historic fitness and some crucial content about health and fitness.</p>

<p><a href="/AdonisEffect.pdf">The Adonis Effect</a> is more geared towards the importance of having a great looking body from a social influence perspective. If you already know you want to look better I'd focus on the workout content and come back to this later, but just the same the content about the social aspects of having and maintaining a great body are hard to ignore in this link.</p>

<h2>Categories</h2>
<p>The different categories of workouts are for different body types and/or different goals. Category 1 is focused on building muscles and is particularly suited to tall slender people who have little body fat and really just need more muscle to fill themselves out. Category 2 is for people in the opposite boat, bigger people who probably have a decent amount of muscle on them already but also have some excess fat they would like to burn. And category 3 is for people who are somewhere in between the first and second categories. Needing to build muscle mass AND burn some fat. This is the most common category people fit into but everybody is different.</p>
<p>So each workout program is broken up into different categories and each category has a coinciding nutrition and supplement plan. You can find them here and for many people this makes the difference between doing something to feel a little better, and someone who totally has every intention of getting the ideal body they are looking for. If you are regularly doing the workouts and would like to take it to the next step this sections would be great for you.</p>

